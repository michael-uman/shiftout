int __io_putchar(int ch) {
	uint8_t c[1];
	c[0] = ch & 0x00FF;
	HAL_UART_Transmit(&huart1, &*c, 1, 10);
	return ch;
}

ShiftRegister sr(data_pin, clock_pin, latch_pin, MSBFIRST);

void write_value(uint8_t value) {
	digitalWrite(latch_pin, LOW);
	sr.write(value);
}

void shiftUp() {
	int i;
	int count = rand() % 10 + 4;
	LOG_MSG("%s\n", "shiftUp()");
	for (int x = 0 ; x < count ; x++) {
		for (i = 0; i <= 7; i++) {
			uint8_t m = (1L << i);
			write_value(m);
			HAL_Delay(100);
		}
	}
}

void shiftDown() {
	int i;
	int count = rand() % 10 + 4;
	LOG_MSG("%s\n", "shiftDown()");

	for (int x = 0 ; x < count ; x++) {
		for (i = 7; i >=  0; i--) {
			uint8_t m = (1L << i);
			write_value(m);
			HAL_Delay(100);
		}
	}
}

void randomPattern() {
	int count = (rand() % 10) + 10;
	int delay = 100 + (rand() % 8) * 100;
	LOG_MSG("%s\n", "randomPattern()");

	for (int i = 0; i < count; i++) {
		int pattern = rand() % 255;
		write_value(pattern);
		HAL_Delay(delay);
	}
}

void pinchIn() {
	int count = rand() % 10 + 4;
	LOG_MSG("%s\n", "pinchIn()");

	for (int j = 0; j < count; j++) {
		for (int i = 0; i < 4; i++) {
			uint8_t mask = (1L << (7 - i)) | ((1L << i));
			write_value(mask);
			HAL_Delay(100);
		}
	}
}
void pinchOut() {
	int count = rand() % 10 + 4;
	LOG_MSG("%s\n", "pinchOut()");

	for (int j = 0; j < count; j++) {
		for (int i = 3; i >= 0 ; i--) {
			uint8_t mask = (1L << (7 - i)) | ((1L << i));
			write_value(mask);
			HAL_Delay(100);
		}
	}
}

void displaySequence(int * data, size_t count) {
	int delay = 100 + (rand() % 8) * 100;
	int repeat = rand() % 10 + 4;
	for (int j = 0; j < repeat; j++) {
		for (size_t i = 0 ; i < count ; i++) {
			write_value(data[i]);
			HAL_Delay(delay);
		}
	}
}

void fillOut() {
	LOG_MSG("%s\n", "fillOut()");
	int masks[] = {
		0x18, // 00011000
		0x3c, // 00111100
		0x7e, // 01111110
		0xff, // 11111111
	};

	displaySequence(masks, sizeof(masks)/sizeof(int));
}

void fillIn() {
	LOG_MSG("%s\n", "fillIn()");

	int masks[] = {
		0x81, // 10000001
		0xc3, // 11000011
		0xe7, // 11100111
		0xff, // 11111111
	};
	displaySequence(masks, sizeof(masks)/sizeof(int));
}

